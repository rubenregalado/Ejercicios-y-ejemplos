    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // Declarar el arreglo
        String Datos[]= new String [3];
        //Definir valor para cada campo ingresado 
        Datos[0]= txtNombre.getText();
        Datos[1]= txtApellido.getText();
        Datos[2]= txtEdad.getText();
        
        modelo.addRow(Datos);
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        rows = this.tabla.getSelectedRow();
        
        if(rows >= 0){
        txtNombre.setText(this.tabla.getValueAt(rows, 0).toString());
        txtApellido.setText(this.tabla.getValueAt(rows, 1).toString());
        txtEdad.setText(this.tabla.getValueAt(rows, 2).toString());
        
        
        }
        
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
       
      this.tabla.setValueAt(txtNombre.getText(), rows, 0);
      this.tabla.setValueAt(txtApellido.getText(), rows, 1);
      this.tabla.setValueAt(txtEdad.getText(), rows, 2);
      
    }//GEN-LAST:event_btnActualizarActionPerformed

