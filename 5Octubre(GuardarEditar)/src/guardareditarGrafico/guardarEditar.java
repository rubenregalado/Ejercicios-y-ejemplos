    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = this.tabla.getSelectedRow();
        if (row >= 0 ){
             int confirmar = JOptionPane.showConfirmDialog(null, "Esta seguro de elminar la fila"," ", JOptionPane.YES_NO_OPTION);
             if (confirmar == 0){
                  modelo.removeRow(row);
                  JOptionPane.showMessageDialog(null, "Se ha eliminado correctamente los datos");
             }
             
        
        }
        else {
              JOptionPane.showMessageDialog(null, "Porfavor seleccione una fila");
             }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

