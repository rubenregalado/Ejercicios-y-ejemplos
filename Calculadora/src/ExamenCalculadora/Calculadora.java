public class Calculadora {
    
    int suma (int a, int b){
        return a + b;
        
    }
    
    int resta (int a, int b){
        return a - b;
        
    }
    
    int division (int a, int b){
        return a/b;
        
    }
    
    int multiplicacion (int a, int b){
        return a + b;
        
    }
    
    int promedio (int a, int b){
        return (a+b)/2;
        
    }
    
    
    
    
    
    
}
